#include "libft.h"
#include <ctype.h>

int	ft_isalnum(int c)
{
	if ((c >= 65 && c <= 90)
		|| (c >= 48 && c <= 57)
		|| (c >= 97 && c <= 122))
		return (c);
	else
		return (0);
}
